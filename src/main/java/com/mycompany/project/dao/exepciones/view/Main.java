    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(txtID.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null, "Rellene el campo");
            txtID.requestFocus();
            return;
        }
        int opt = JOptionPane.showConfirmDialog(null, "Desea continuar con la eliminacion?", "Confirmar salida", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if(opt == 0){
            try {
                funcionarioController.deleteFunc(Integer.parseInt(txtID.getText()));

                txtTpD.setText("");
                txtNDoc.setText("");
                txtNomb.setText("");
                txtApell.setText("");
                txtEssCiv.setText("");
                txtSexGener.setText("");
                txtDirc.setText("");
                txtTelleff.setText("");
                txtNaciFechh.setText("");
                listarFunc();
                JOptionPane.showMessageDialog(null, "Registro actualizado con exito");

            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Problemas a la hora de Eliminar el registro");
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

